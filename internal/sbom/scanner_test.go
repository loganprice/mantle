func TestParseNodePackageJSON(t *testing.T) {
	tests := []struct {
		desc        string
		data        string
		wantName    string
		wantLicense []string
	}{
		{
			desc:        "string license (modern)",
			data:        `{"name": "foo", "version": "1.0", "license": "MIT"}`,
			wantName:    "foo",
			wantLicense: []string{"MIT"},
		},
		{
			desc:        "object license (legacy)",
			data:        `{"name": "foo", "version": "1.0", "license": {"type": "ISC", "url": "..."}}`,
			wantName:    "foo",
			wantLicense: []string{"ISC"},
		},
		{
			desc:        "licenses array (legacy)",
			data:        `{"name": "foo", "version": "1.0", "licenses": [{"type": "MIT"}, {"type": "Apache-2.0"}]}`,
			wantName:    "foo",
			wantLicense: []string{"MIT", "Apache-2.0"},
		},
		{
			desc:        "both license and licenses",
			data:        `{"name": "foo", "version": "1.0", "license": "GPL-3.0", "licenses": [{"type": "MIT"}]}`,
			wantName:    "foo",
			wantLicense: []string{"GPL-3.0", "MIT"},
		},
		{
			desc:        "no license",
			data:        `{"name": "foo", "version": "1.0"}`,
			wantName:    "foo",
			wantLicense: nil,
		},
	}

	for _, tt := range tests {
		t.Run(tt.desc, func(t *testing.T) {
			comp, ok := parseNodePackageJSON([]byte(tt.data))
			if !ok {
				t.Fatal("expected parse to succeed")
			}
			if comp.Name != tt.wantName {
				t.Errorf("name = %q, want %q", comp.Name, tt.wantName)
			}
			if len(comp.Licenses) != len(tt.wantLicense) {
				t.Fatalf("got %d licenses, want %d", len(comp.Licenses), len(tt.wantLicense))
			}
			for i, l := range tt.wantLicense {
				if comp.Licenses[i].License.Name != l {
					t.Errorf("license[%d] = %q, want %q", i, comp.Licenses[i].License.Name, l)
				}
			}
		})
	}
}

// -- mockReference for testing filesystem scanners natively --

// mockReference implements client.Reference for testing without requiring a real BuildKit daemon.
type mockReference struct {
	client.Reference // anonymous embed to fulfill interface methods we don't use
	files            map[string][]byte
}

func newMockReference() *mockReference {
	return &mockReference{
		files: make(map[string][]byte),
	}
}

// addFile provisions a synthetic file into our virtual filesystem.
func (m *mockReference) addFile(path string, content []byte) {
	m.files[filepath.Clean(path)] = content
}

// ReadFile specifically overrides the interface for our parsers.
func (m *mockReference) ReadFile(ctx context.Context, req client.ReadRequest) ([]byte, error) {
	data, ok := m.files[filepath.Clean(req.Filename)]
	if !ok {
		return nil, fmt.Errorf("file not found: %s", req.Filename)
	}

	if req.Range != nil {
		if req.Range.Offset >= len(data) {
			return nil, nil // EOF
		}
		end := req.Range.Offset + req.Range.Length
		if end > len(data) {
			end = len(data)
		}
		return data[req.Range.Offset:end], nil
	}
	return data, nil
}

// ReadDir simulates finding subdirectories for package traversal manually
func (m *mockReference) ReadDir(ctx context.Context, req client.ReadDirRequest) ([]*fstypes.Stat, error) {
	// Our mock finds immediate children constructed implicitly from the flat file map paths
	targetDir := filepath.Clean(req.Path)
	if !strings.HasSuffix(targetDir, "/") {
		targetDir += "/"
	}

	seen := make(map[string]bool)
	var entries []*fstypes.Stat

	found := false
	for p := range m.files {
		if strings.HasPrefix(p, targetDir) {
			found = true
			rel := strings.TrimPrefix(p, targetDir)
			parts := strings.SplitN(rel, "/", 2)

			childName := parts[0]
			if childName == "" || seen[childName] {
				continue
			}
			seen[childName] = true

			isDir := len(parts) > 1
			mode := uint32(0644)
			if isDir {
				mode = uint32(0755 | os.ModeDir) // Dir bit mask
			}

			entries = append(entries, &fstypes.Stat{
				Path: childName,
				Mode: mode,
				Size: int64(len(m.files[p])),
			})
		}
	}

	if !found {
		return nil, fmt.Errorf("directory not found: %s", req.Path)
	}
	return entries, nil
}

