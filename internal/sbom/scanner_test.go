func TestScanner_Go(t *testing.T) {
	mock := newMockReference()

	// Compile a tiny valid ELF Go binary dynamically so buildinfo.Read can parse a real Linux binary organically.
	tmpDir, err := os.MkdirTemp("", "mantle-go-test-")
	if err != nil {
		t.Fatalf("failed to create temp dir: %v", err)
	}
	defer os.RemoveAll(tmpDir)

	srcFile := filepath.Join(tmpDir, "main.go")
	_ = os.WriteFile(srcFile, []byte(`package main; import _ "fmt"; func main() {}`), 0644)

	binFile := filepath.Join(tmpDir, "app")
	cmd := exec.Command("go", "build", "-o", binFile, srcFile)
	cmd.Env = append(os.Environ(), "GOOS=linux", "GOARCH=amd64") // Force ELF compilation
	if err := cmd.Run(); err != nil {
		t.Fatalf("failed to compile dummy ELF go binary: %v", err)
	}

	data, err := os.ReadFile(binFile)
	if err != nil {
		t.Fatalf("failed to read compiled dummy: %v", err)
	}

	mock.addFile("/usr/local/bin/my-go-app", data)

	scanner := &goScanner{}
	comps := scanner.Scan(context.Background(), mock)

	// An empty inline main.go has no external modules in its buildinfo deps
	if len(comps) != 0 {
		t.Fatalf("expected 0 external components for empty dummy program, got %d", len(comps))
	}
}

