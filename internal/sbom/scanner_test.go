func TestParseDepsJSON(t *testing.T) {
	data := []byte(`{
  "libraries": {
    "Newtonsoft.Json/13.0.3": {
      "type": "package"
    },
    "Microsoft.Extensions.Logging/8.0.0": {
      "type": "package"
    },
    "MyApp/1.0.0": {
      "type": "project"
    }
  }
}`)

	components := parseDepsJSON(data)

	// Should have 2 (project refs are skipped)
	if len(components) != 2 {
		t.Fatalf("expected 2 components, got %d", len(components))
	}

	found := make(map[string]bool)
	for _, c := range components {
		found[c.Name] = true
		if c.Name == "Newtonsoft.Json" {
			if c.Version != "13.0.3" {
				t.Errorf("version = %q, want 13.0.3", c.Version)
			}
			if c.PURL != "pkg:nuget/Newtonsoft.Json@13.0.3" {
				t.Errorf("purl = %q, want pkg:nuget/Newtonsoft.Json@13.0.3", c.PURL)
			}
		}
	}

	if !found["Newtonsoft.Json"] || !found["Microsoft.Extensions.Logging"] {
		t.Errorf("missing expected packages, found: %v", found)
	}
	if found["MyApp"] {
		t.Error("project reference MyApp should have been skipped")
	}
}

